// Helper function - get mock products with unique IDs
export const getMockProducts = (count = 10) => {
    let prods = [...MOCK_PRODUCTS];
    while (prods.length < count) {
        prods = [...prods, ...MOCK_PRODUCTS.map(p => ({
            ...p,
            id: p.id + '-' + Math.random().toString(36).substr(2, 9)
        }))];
    }
    return prods.slice(0, count);
};

// Get products by collection
export const getProductsByCollection = (collection) => {
    switch (collection) {
        case 'freshDrops':
        case 'fresh':
            return FRESH_DROPS;
        case 'bestseller':
        case 'bestsellers':
            return BESTSELLERS;
        case 'trending':
            return TRENDING;
        case 'under50':
            return UNDER_50;
        default:
            return MOCK_PRODUCTS;
    }
};

